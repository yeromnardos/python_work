# list datastructure

my_list = ["Kenya", "Nigeria", "Tanzania", "Kenya", "Uganda", "Brazil", "Sweden"]
my_num = [2, 8, 1, 34, 27, 9, 0, -1, 23]
my_list.sort()
print(my_list)
my_num.sort()
print(my_num)
my_list[2] = "South Africa"
print(my_list[2])
print(f"I am from {my_list[0]}")
print(len(my_list))

# tuple datastructures
my_tuple = ("Banana", "Apple", "Mango", "Watermelon")
print(my_tuple)

# set datastructure

my_set = {"Toyota", "Nissan", "BMW", "Mercedes", "Subaru"}
print(my_set)
my_set.add("mazda")
# dictionaries datastructure
my_dic = {"name": "Erick", "age": 47, "gender": "male"}
print(my_dic)
print(f"my age is {my_dic['age']}")
